//object access properties

var num={
    w:10,
    x:15,
    y:20,
    z:30

}

//Dot notation 

console.log(num.w)
console.log(num.y)
console.log(num.x+num.z)
console.log(num.x*num.y)
console.log(num.z/num.w)


//Array Notation

console.log(num['w'])
console.log(num['y'])
console.log(num['x']+num['z'])


